//////////////////////////////////////////////////////
// REQUIRE MODULES
//////////////////////////////////////////////////////
const express = require("express");

//////////////////////////////////////////////////////
// CREATE ROUTER
//////////////////////////////////////////////////////
const router = express.Router();

//////////////////////////////////////////////////////
// DEFINE ROUTES
//////////////////////////////////////////////////////
const jwtMiddleware = require("../middlewares/jwtMiddleware");
const userController = require("../controllers/userController");

router.get('/', userController.readAllUser);

router.get("/profile", jwtMiddleware.verifyToken, userController.getProfile);

router.get('/token/player', jwtMiddleware.verifyToken, userController.getAllPlayerByUserId);

router.get('/:id', userController.readUserById);

//////////////////////////////////////////////////////
// EXPORT ROUTER
//////////////////////////////////////////////////////
module.exports = router;